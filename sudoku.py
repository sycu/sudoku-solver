from numpy import array
from typing import Generator, List

SudokuGrid = List[List[int]]


class SudokuSolver:
    def solve(self, sudoku: SudokuGrid) -> Generator[SudokuGrid, None, None]:
        for y in range(9):
            for x in range(9):
                if not sudoku[y][x]:
                    for value in range(1, 10):
                        if self.__is_valid_move(sudoku, x, y, value):
                            sudoku[y][x] = value
                            yield from self.solve(sudoku)
                            sudoku[y][x] = 0

                    return None

        yield sudoku

    def __is_valid_move(self, sudoku: SudokuGrid, x: int, y: int, value: int) -> bool:
        for i in range(9):
            if sudoku[y][i] == value or sudoku[i][x] == value:
                return False

        x0 = (x // 3) * 3
        y0 = (y // 3) * 3
        for j in range(y0, y0 + 3):
            for i in range(x0, x0 + 3):
                if sudoku[j][i] == value:
                    return False

        return True


